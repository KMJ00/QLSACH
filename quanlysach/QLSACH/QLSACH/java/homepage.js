    $(document).ready(function(){
    var owlCarouselOptions = {
        loop: true,
        autoplay: true,
        autoplayTimeout: 3000,
        autoplaySpeed: 1500,
        margin: 10,
        dots: false,
    };

    function updateCarousel() {
        if ($(window).width() <= 1024) {
            // Kích thước màn hình nhỏ hơn hoặc bằng1536px
            owlCarouselOptions.items = 3;

             if ($(window).width() <= 740) {
            // Kích thước màn hình nhỏ hơn   hoặc bằng1536px
            owlCarouselOptions.items = 2; 
        }
        } 
        else {
            // Kích thước màn hình lớn hơn 600px
            owlCarouselOptions.items = 4;
        }

        // Hủy carousel hiện tại (nếu có)
        $('.owl-carousel').trigger('destroy.owl.carousel');

        // Khởi tạo carousel với cấu hình mới
        $('.owl-carousel').owlCarousel(owlCarouselOptions);
    }

    // Khởi tạo carousel ban đầu
    updateCarousel();

    // Cập nhật cấu hình khi kích thước màn hình thay đổi
    $(window).on('resize', function() {
        updateCarousel();
    });
});